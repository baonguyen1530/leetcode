class Solution:
    def lengthOfLIS(self, nums: List[int]) -> int:
        LIS = [1] * len(nums)

        for i in range(len(nums) - 1, -1, -1):
            for j in range(i + 1, len(nums)):
                if nums[i] < nums[j]:
                    LIS[i] = max(LIS[i], 1 + LIS[j])

        return max(LIS)

'''
1. Iterate through the array backward (i), have a nested for loop for a certain segment (j)
2. As we iterate through that segment, check if j > i, if so that segment is strictly increasing
'''